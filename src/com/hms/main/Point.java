package com.hms.main;

public class Point {
  
  private int xCor = 0;
  private int yCor = 0;
  
  public Point(int x, int y) {
    
    this.xCor = x;
    this.yCor = y;
    
  }

  public int getxCor() {
    return xCor;
  }

  public void setxCor(int xCor) {
    this.xCor = xCor;
  }

  public int getyCor() {
    return yCor;
  }

  public void setyCor(int yCor) {
    this.yCor = yCor;
  }
  
  

}
