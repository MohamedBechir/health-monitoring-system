package com.hms.main;

public class Point {
  
  private float xCor = 0;
  private float yCor = 0;
  
  public Point(float x, float y) {
    
    this.xCor = x;
    this.yCor = y;
    
  }

  public float getxCor() {
    return xCor;
  }

  public void setxCor(int xCor) {
    this.xCor = xCor;
  }

  public float getyCor() {
    return yCor;
  }

  public void setyCor(int yCor) {
    this.yCor = yCor;
  }
  
  

}
